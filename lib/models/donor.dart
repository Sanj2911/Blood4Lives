// Donor Profile Model
class DonorProfile {
  final String fullName;
  final String age;
  final String nic;
  final String address;
  final String gender;
  final String bloodType;
  final String email;
  final String phone;

  DonorProfile({
    required this.fullName,
    required this.age,
    required this.nic,
    required this.address,
    required this.gender,
    required this.bloodType,
    required this.email,
    required this.phone,
  });
}

